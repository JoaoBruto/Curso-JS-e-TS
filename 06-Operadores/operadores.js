// Aritméticos: + - / *
// + = Adição/Concatenação
// - = Subtração
// / = Divisão
// * = Multiplicação
// ** = Potenciação
// % = Resto da divisão

const num1 = 15
const num2 = 5
console.log(num1 + num2)

//Incremento = ++
//Decremento = --
let contador = 10;
contador--;
console.log(contador)

// Atribuição
let numero = 2;
numero += 2; // Mesma coisa que numero = numero + 2
console.log(numero)